// round
const bilangan = 1.9;
const hasil = Math.round(bilangan);
console.log(hasil);

// ceil
const bilangan = 1.9;
const hasil = Math.ceil(bilangan);
console.log(hasil);

// floor
const bilangan = 1.9;
const hasil = Math.floor(bilangan);
console.log(hasil);

// min
const bilangan = [1, 4, 5, 8, 10];
const hasil = Math.min(...bilangan);
console.log(hasil);

// max
const bilangan = [1, 4, 5, 8, 10];
const hasil = Math.max(...bilangan);
console.log(hasil);

// random
const nomor = Math.random();
console.log(nomor);